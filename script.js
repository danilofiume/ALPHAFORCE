document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('language') || 'en'; // Default in inglese
    changeLanguage(savedLang);
});



function changeLanguage(lang) {
    localStorage.setItem('language', lang); // Salva la lingua nel localStorage
    
    const data = translations[lang];
    if (data) {
        document.querySelectorAll('[data-key]').forEach(element => {
            const key = element.getAttribute('data-key');
            element.textContent = data[key];
        });

        // Rimuovi il grassetto da tutti i bottoni
        document.getElementById('btn-en').classList.remove('selected-language');
        document.getElementById('btn-it').classList.remove('selected-language');

        // Aggiungi il grassetto solo al bottone della lingua selezionata
        document.getElementById(`btn-${lang}`).classList.add('selected-language');
    } else {
        console.error('Translation not found for language:', lang);
    }
}
  


// Funzione per mostrare il loader
function showLoader() {
  const loader = document.getElementById('loader');
  loader.classList.add('active');
}

// Funzione per nascondere il loader
function hideLoader() {
  const loader = document.getElementById('loader');
  loader.classList.remove('active');
}

// Mostra il loader quando la pagina inizia a cambiare
window.addEventListener('beforeunload', showLoader);

// Nascondi il loader quando la pagina è caricata
window.addEventListener('load', hideLoader);


// Quando la pagina è caricata
window.addEventListener('load', () => {
  document.body.classList.add('visible'); // Aggiungi la classe per mostrare il body
});

// Se hai bisogno di gestire il caricamento della nuova pagina
document.querySelectorAll('a[data-url]').forEach(link => {
  link.addEventListener('click', (event) => {
      event.preventDefault(); // Previeni il comportamento predefinito
      const newUrl = link.getAttribute('data-url'); // Ottieni l'URL dal data-attribute
      
      // Imposta l'opacità a 0 per il corpo prima di caricare la nuova pagina
      document.body.classList.remove('visible');
      
      // Aspetta un breve momento prima di cambiare pagina
      setTimeout(() => {
          window.location.href = newUrl;
      }, 500); // Tempo che corrisponde alla durata della transizione di opacità
  });
});


window.addEventListener('scroll', () => {
  const header = document.getElementById('header');
  if (window.scrollY > 10) {
      header.classList.add('scrolled');
  } else {
      header.classList.remove('scrolled');
  }
});





